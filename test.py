import sqlite3


def crearBaseNodos():
    conn = sqlite3.connect(NombreBD)
    cursor = conn.cursor()
    cursor.execute('''
        CREATE TABLE IF NOT EXISTS nodo (
            ID INTEGER PRIMARY KEY AUTOINCREMENT,
            Nombre TEXT NOT NULL,
            Latitud REAL,
            Longitud REAL
        )
    ''')
    conn.commit()

def insertarNodo(id, nombre, latitud, longitud):
    try:
        conn = sqlite3.connect(NombreBD)
        cursor = conn.cursor()
        cursor.execute('''
            INSERT INTO nodo (ID, Nombre, Latitud, Longitud)
            VALUES (?, ?, ?, ?)
        ''', (id, nombre, latitud, longitud))
        conn.commit()
    except sqlite3.IntegrityError:
        print(f"Error: El ID {id} ya existe.")
    finally:
        conn.close()


def crearBaseEstacionamiento():
    conn = sqlite3.connect(NombreBD)
    cursor = conn.cursor()
    cursor.execute('''
        CREATE TABLE IF NOT EXISTS estacionamiento (
            ID INTEGER PRIMARY KEY,
            Valoracion INTEGER,
            Plaza INTEGER,
            NodoID INTEGER,
            FOREIGN KEY (NodoID) REFERENCES nodo(ID)
        )
    ''')
    conn.commit()
    conn.close()

def insertarEstacionamiento(id, nombre, latitud, longitud, valoracion, plaza):
    conn = sqlite3.connect(NombreBD)
    cursor = conn.cursor()
    try:
        # Insertar en la tabla nodo usando el ID proporcionado
        cursor.execute('''
            INSERT INTO nodo (ID, Nombre, Latitud, Longitud)
            VALUES (?, ?, ?, ?)
        ''', (id, nombre, latitud, longitud))
        
        # Insertar en la tabla estacionamiento usando el ID del nodo como NodoID
        cursor.execute('''
            INSERT INTO estacionamiento (Valoracion, Plaza, NodoID)
            VALUES (?, ?, ?)
        ''', (valoracion, plaza, id))
        
        conn.commit()
    except sqlite3.IntegrityError as e:
        print(f"Error al insertar en estacionamiento: {e}")
    finally:
        conn.close()


def crearArista():
    conn = sqlite3.connect(NombreBD)
    cursor = conn.cursor()
    cursor.execute('''
        CREATE TABLE IF NOT EXISTS arista (
            ID INTEGER PRIMARY KEY,
            NodoInicio INTEGER,
            NodoFinal INTEGER,
            Distancia REAL,
            FOREIGN KEY (NodoInicio) REFERENCES nodo(ID),
            FOREIGN KEY (NodoFinal) REFERENCES nodo(ID)
        )
    ''')
    conn.commit()
    conn.close()

def insertarArista(id, nodo_inicio, nodo_final, distancia):
    conn = sqlite3.connect(NombreBD)
    cursor = conn.cursor()
    try:
        cursor.execute('''
            INSERT INTO arista (ID, NodoInicio, NodoFinal, Distancia)
            VALUES (?, ?, ?, ?)
        ''', (id, nodo_inicio, nodo_final, distancia))
        conn.commit()
    except sqlite3.IntegrityError:
        print(f"Error: El ID {id} ya existe o las referencias a nodos no son válidas.")
    finally:
        conn.close()



